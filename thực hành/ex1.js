// Sử dụng vòng lặp for
for (let i = 1; i <= 10; i++) {
    document.write('Sinh viên UDU ! <br>');
}

// Sử dụng vòng lặp while
let count = 1;
while (count <= 10) {
    document.write('Sinh viên UDU ! <br>');
    count++;
}

// Sử dụng vòng lặp do while
let num = 1;
do {
    document.write('Sinh viên UDU ! <br>');
    num++;
} while (num <= 10);
